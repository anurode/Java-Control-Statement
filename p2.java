//Number is greater than 10 or equal to 10

class Demo {

    public static void main(String args[]){

        int var = 5;

        if(var > 10){
            System.out.println(var+ "is greater than 10");
        }else if(var < 10){
            System.out.println(var+ "is smaller than 10");
        }else{
            System.out.println(var+ "is equal to 10");
        }
    }
}